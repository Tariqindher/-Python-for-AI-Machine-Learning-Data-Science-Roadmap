# Week 01 - Data Types Practice
# Just playing with basic Python types
name = "Tariq"
age = 23
height = 5.4
is_student = True

print(name)
print(age)
print(height)
print(is_student)

print(type(name))
print(type(age))
print(type(height))
print(type(is_student))

# list - just some fruits I like
fruits = ["apple", "banana", "mango"]
print(fruits)

# tuple - can't change it
colors = ("red", "green", "blue")
print(colors)

# set - removes duplicates automatically
numbers = {1, 2, 2, 3, 4, 4, 5}
print(numbers)

# dictionary - like a mini database
student = {
    "name": "Ali",
    "age": 22,
    "city": "Karachi"
}

print(student)
print(student["name"])

